/* USER CODE BEGIN 0 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if (htim->Instance == TIM2)
  {
    HAL_ADC_Start(&hadc1);
    HAL_ADC_PollForConversion(&hadc1, 10);
    float sample = (float)HAL_ADC_GetValue(&hadc1) / 4095.0f;

    circ_buf[circ_head] = sample;

    /* Pan-Tompkins derivative: y = (2x[n] + x[n-1] - x[n-3] - 2x[n-4]) / 8 */
    float deriv = (2.0f*pt_d[0] + pt_d[1] - pt_d[3] - 2.0f*pt_d[4]) / 8.0f;
    pt_d[4] = pt_d[3]; pt_d[3] = pt_d[2];
    pt_d[2] = pt_d[1]; pt_d[1] = pt_d[0];
    pt_d[0] = sample;

    /* Square + 30-sample moving window integration */
    float sq = deriv * deriv;
    pt_mwi_sum += sq - pt_mwi_buf[pt_mwi_idx];
    pt_mwi_buf[pt_mwi_idx] = sq;
    pt_mwi_idx = (pt_mwi_idx + 1) % 30;
    float mwi = pt_mwi_sum / 30.0f;

    if (pt_refractory > 0) pt_refractory--;

    /* R-peak: local maximum above threshold, outside refractory period */
    if (!r_peak_found && !beat_ready)
    {
      if ((pt_prev >= pt_threshold) && (pt_refractory == 0) && (mwi < pt_prev))
      {
        r_peak_found       = 1;
        r_peak_pos         = (circ_head - 1 + CIRC_SIZE) & CIRC_MASK;
        samples_after_peak = 0;
        if (pt_prev > pt_peak) pt_peak = pt_prev;
        pt_threshold  = pt_peak * 0.5f;
        pt_refractory = 72;  /* 200 ms at 360 Hz */
      }
      else if (mwi < pt_threshold)
      {
        pt_threshold -= pt_threshold * 0.008f;  /* slow decay toward noise floor */
      }
    }
    pt_prev = mwi;

    /* After R-peak, wait BEAT_HALF samples then extract centered window */
    if (r_peak_found)
    {
      if (++samples_after_peak >= BEAT_HALF)
      {
        uint16_t start = (r_peak_pos - BEAT_HALF + 1 + CIRC_SIZE) & CIRC_MASK;
        for (int i = 0; i < BEAT_SIZE; i++)
          ecg_buffer[i] = circ_buf[(start + i) & CIRC_MASK];

        /* Z-score normalize — must match training: (beat - mean) / std */
        float mean = 0.0f, std = 0.0f;
        for (int i = 0; i < BEAT_SIZE; i++) mean += ecg_buffer[i];
        mean /= BEAT_SIZE;
        for (int i = 0; i < BEAT_SIZE; i++) { float d = ecg_buffer[i] - mean; std += d * d; }
        std = sqrtf(std / BEAT_SIZE);
        if (std < 1e-6f) std = 1e-6f;
        for (int i = 0; i < BEAT_SIZE; i++) ecg_buffer[i] = (ecg_buffer[i] - mean) / std;

        r_peak_found = 0;
        beat_ready   = 1;
      }
    }

    circ_head = (circ_head + 1) & CIRC_MASK;
  }
}
